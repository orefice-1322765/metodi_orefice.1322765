/**
 * Lezione 8 - Punto
 * 
 * Progettare una classe Punto per la rappresentazione di un punto nello spazio tridimensionale
 * 
 * @author forefice
 */
public class Punto {
	private int x, y, z;

	/**
	 * Costruttore della classe
	 * @param altezza
	 * @param larghezza
	 * @param profondità
	 */
	public Punto(int x, int y, int z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
}